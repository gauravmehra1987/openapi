					$this->load->model('account/address');

                                        $name = explode(' ', $this->request->post['firstname']);
                                        $this->request->post['firstname'] = isset($name[0])?$name[0]:'';
                                        $this->request->post['lastname'] = isset($name[1])?$name[1]:'';
                                        $this->request->post['fax'] = '';
                                        $this->request->post['address_2'] = '';
                                        $this->request->post['company'] = '';
                        
